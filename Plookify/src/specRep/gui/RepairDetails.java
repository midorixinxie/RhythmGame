    private void searchVehicleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchVehicleActionPerformed
        String selectedSPC=(String)selectSPCs.getSelectedItem();
        Database db = new Database();
        db.connect();
        DefaultTableModel model=(DefaultTableModel)jTable1.getModel();
        try{
            Connection mycon=db.getConnection();  
            String query="SELECT Name,RegistrationNumber,expectedDeliveryDate,costsOfSpecialistRepair,expectedReturnDate FROM specialistRepairCenter where RegistrationNumber="+Long.parseLong(jTextField1.getText())+"";
            pst=mycon.prepareStatement(query);
            //pst.setString(1,"Value");
            rs =pst.executeQuery();
            
           
            int i=0;
            while (rs.next()){
              
                String name=rs.getString("Name");
	        String RegistrationNumber = rs.getString("RegistrationNumber");
	        String expectedDeliveryDate = rs.getString("expectedDeliveryDate");
                String costsOfSpecialistRepair= rs.getString("costsOfSpecialistRepair");
                String expectedReturnDate = rs.getString("expectedReturnDate");
                model.addRow(new Object[]{name,RegistrationNumber,expectedDeliveryDate, costsOfSpecialistRepair, expectedReturnDate});
                
                i++;
     
	  }
            if (i < 1) {
                JOptionPane.showMessageDialog(null, "No Record Found", "Error", JOptionPane.ERROR_MESSAGE);
            }
            if (i == 1) {
                System.out.println(i + " Record Found");
            } else {
                System.out.println(i + " Records Found");
            }
          rs.close();
            pst.close();
          
        }
           
           
        catch(SQLException e){
                 JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_searchVehicleActionPerformed

