    private void nameActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nameActionPerformed
        // TODO add your handling code here:
        switch((String)name.getSelectedItem()){
            case "Spark Plugs":
                cost.setText("20");
                break;
            case "Prop shaft":
                cost.setText("25");
                break;
            case "Handbrake cable":
                cost.setText("30");
                break;
            case "Timing belt":
                cost.setText("35");
                break;
            case "Fan belt":
                cost.setText("50");
                break;
            case "Bumpers":
                cost.setText("75");
                break;
            case "Tyre rim":
                cost.setText("50");
                break;
            case "AC evaporator coil":
                cost.setText("45");
                break;
            case "Brake disk":
                cost.setText("70");
                break;
            case "Air filter":
                cost.setText("25");
                break;
            default: 
                cost.setText("0");
                break;
        }
    }//GEN-LAST:event_nameActionPerformed

